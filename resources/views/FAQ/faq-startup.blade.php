<h3 style="text-transform: none;">Why seek an Angel Investor?</h3>
A good angel investor provides business assistance in many ways: • They will provide the money you need to set up or grow your business. • They might take a seat on your board of directors and play an active role in the day-to-day management of your business. • They’re usually current or former business owners themselves, so can offer advice on business management and strategy. • They often have a large network of contacts they can introduce you to - more angels, potential partners, suppliers, employees, etc. • They can help your expand your client base by promoting your business to colleagues and friends.</br>
